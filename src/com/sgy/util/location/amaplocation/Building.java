package com.sgy.util.location.amaplocation;

/**
 * 建筑物
 * @author yuanfy
 * @date 2014-11-04
 */
public class Building {
    
    // 建筑物名称
    private String name = "";
    
    // 建筑物类型
    private String type = "";

    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
